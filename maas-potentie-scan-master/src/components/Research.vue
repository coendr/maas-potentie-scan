    <h6><b>4. Conclusies</b></h6>
    <p>
      In dit paper hebben we laten zien dat het rekenen aan MaaS begint met het
      bepalen van het marktaandeel, ofwel de potentie voor een gegeven MaaS
      propositie. Daarvoor is een gedragsmodel nodig wat we in dit paper hebben
      geleend uit Australie. Op basis van dit gedragsmodel en data over de
      populatie (op basis van OViN en CBS buurt data) het huidig
      mobiliteitsgedrag (op basis van GSM data) van CBS kunnen MaaS proposities
      die te beschrijven zijn met variabelen beschikbaar in het gedragsmodel
      worden doorgerekend. We hebben dit gedemonstreerd aan de hand van een
      viertal MaaS proposities, aan de randen van het gebruikte gedragsmodel. De
      resultaten zijn onderling consistent en het model geeft inzicht in de tot
      stand koming van de resultaten.<br /><br />
      Er kan nog een hoop energie gestopt worden in het verder verklaren van de
      modelresultaten en het onderzoeken van legio MaaS proposities. Echter, de
      Australische dataset die ten grondslag ligt aan het gebruikte gedragsmodel
      is niet representatief voor de Nederlandse situatie. Zo is er bijvoorbeeld
      geen parameter voor parkeerkosten omdat deze in Australie niet of
      nauwelijks aanwezig zijn, en kon geen significante parameter voor OV
      ticketkosten (binnen de betalen naar gebruik abonnementsvorm) worden
      gevonden door het bestaan van een plafond bedrag per week voor OV
      ticketkosten in de Australische situatie. Een ander voorbeeld is de
      situatie voor wat betreft de onkostenvergoeding van werkgevers, die in
      Nederland beduidend anders is als in Australie.<br /><br />
      Om deze reden stellen de auteurs dat er, zolang MaaS nog niet op grote
      schaal is uitgerold, stated preference onderzoek nodig is om een
      gedragsmodel te schatten dat de MaaS potentie voor de Nederlandse situatie
      en voor in Nederland relevante MaaS proposities door te rekenen. Dit
      gedragsmodel kan vervolgens direct gebruikt worden binnen de MaaS
      potentiescan. De auteurs zijn voornemens een dergelijk onderzoek op korte
      termijn gestalte te geven en hopen dat dit paper bijdraagt aan actieve
      bijdrage(n) vanuit de markt (gebruikers en ontwikkelaars) aan dit
      onderzoek.<br /><br />
    </p>
    <h6><b>5. Doorkijk: van potentie naar beleid</b></h6>
    <p>
      Uiteindelijk willen we MaaS in zetten om beleidsdoelstellingen, zoals
      bereikbaarheids- en duurzaamheidsopgave aan te kunnen pakken. Hiervoor
      moet een doorvertaling plaats vinden van potentie naar beleid. De
      potentiescan is randvoorwaardelijk om de doorvertaling te kunnen maken om
      de impact (effecten op wegvakniveau zoals emissies, intensiteiten en
      voertuigverliesuren) in beeld te kunnen brengen zodat daar beleid op kan
      worden gevormd. In figuur 3 is de beoogde methodiek voor deze
      doorvertaling weergegeven.<br /><br />
      <img src="../assets/img/figuur6.png" class="img-fluid" /><br />
      Figuur 6: beoogde methodiek voor doorvertaling van potentie naar impact<br /><br />
      Stap 1 is de koppeling van het huidig verkeersmodel met de MaaS
      potentiescan. Door deze koppeling kunnen de matrices opgedeeld worden in
      traditioneel en mogelijk MaaS gebruik.<br /><br />
      Stap 2 van de puzzel bevat nog een kennishiaat naar de effecten op de
      vervoerwijzekeuze en het gebruik van deelproducten. Op dit moment wordt
      een literatuurstudie5 uitgevoerd naar het gedrag van abonnementsbezitters,
      waarbij de hoofdvraag is welke keuzes abonnementhouders maken. Welke
      ritten worden vervangen door het MaaS abonnement (reguliere of enkele
      ritten, welk motief, openbaar vervoer ritten of autoritten) om de matrix
      op te kunnen delen in wel een MaaS abonnement, maar gebruikt traditioneel
      vervoer en abonnementhouders die gebruik maken van MaaS.<br /><br />
      De laatste stap is om via een multi-user class toedeling6 de 3 matrices
      samen te voegen tot het verwacht verkeersbeeld voor de gewenste propositie
      en het gewenste scenario.<br /><br />
      <sup>5</sup> De literatuurstudie wordt ook uitgevoerd op basis van
      buitenlands onderzoek.<br />
      <sup>6</sup> een toedeling waarbij per verschillende usereenheid
      verschillende kosten meegenomen kunnen worden en kan leiden tot andere
      routekeuzes, maar wel gebruik maken van dezelfde aanbod aan infrastructuur
      dus afhankelijk zijn van elkaar.<br /><br />
