      <i>Gedragsmodel </i><br />
      Het heteroskedastisch discrete keuze model uit (Ho et al., 2017) wordt
      toegepast als binary logit model per zone per persoonssegment. Hieruit
      volgt het aandeel van de bevolking dat het MaaS abonnement zou aanschaffen
      per zone per segment. Merk op dat niet significante variabelen zijn
      weggelaten en dat de gemiddelde waarden van de twee random parameters in
      het model (aantal dagen openbaar vervoer gebruik in bundel en aantal uur
      deel autogebruik in bundel) worden gebruikt.<br /><br />
      Door de berekende aandelen per zone per segment te vermenigvuldigen met
      het aantal synthetische personen per zone per segment worden aandelen
      vertaald naar absolute aantallen. Deze kunnen gesommeerd over segmenten
      weergegeven worden op een kaart met absolute aantallen MaaS
      abonnementhouders. Dit geeft inzicht in locaties en regio’s waar veel
      potentiele MaaS gebruikers zitten. Eventueel kan ook gekeken worden naar
      relatieve aandelen MaaS gebruikers door te delen door het aantal inwoners
      per zone. Hiermee wordt het effect van buurten met een afwijkende
      bevolkingsdichtheid of oppervlakte uitgesloten in de uitkomsten.<br /><br />
    </p>
