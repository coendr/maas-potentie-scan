    <h6><b>1. MaaS als nieuw mobiliteitsconcept</b></h6>
    <i>1.1 Wat is MaaS en wat kan dit concept ons bieden</i><br />
    <p>
      De wereld is aan het veranderen. Na de crisisjaren 2008-2014 is er een
      economisch herstel te zien waarbij nieuwe trends en ontwikkelingen
      versterkt en snel zichtbaar worden. Mobility as a Service (MaaS) is een
      zo`n nieuw mobiliteitsconcept waarin reizigers hun ritten kunnen plannen,
      kiezen en betalen via één serviceprovider.<br /><br />
      Op basis van een applicatie wordt door deze serviceprovider aan de
      reiziger informatie beschikbaar gesteld over de reizigersaantallen,
      actuele verkeersinformatie, infrastructuur, beschikbaarheid van voertuigen
      en kosten, om een afweging te maken van de mogelijke alternatieven in
      vervoermiddelen en routekeuzen om tot een persoonlijk reisadvies te
      komen.<br /><br />
      Hierdoor is het mogelijk om collectief vervoer te flexibiliseren, waardoor
      vraag en aanbod beter op elkaar aansluiten waarmee een
      kwaliteitsverbetering in het collectief vervoer kan worden gerealiseerd.
      Daarnaast kan MaaS ook bijdragen in de bereikbaarheids- en
      duurzaamheidsopgave. Het is daarom zeer interessant op inzicht te krijgen
      in de effecten van MaaS op:<br />
       intensiteiten, files, afwikkeling, voertuigverliesuren,<br />
       het gebruik van het bestaande openbaar vervoersysteem,<br />
       het autogebruik in regio,  uitstoot van CO2,NOx, PM10 en/of
      geluidemissies.<br /><br />
      De vraag is echter hoe we de effecten van MaaS kunnen bepalen. Omdat MaaS
      in de kinderschoenen staat, is er nog maar weinig bekend en zijn er nog
      geen kwantitatieve studies in Nederland uitgevoerd. Wel zijn er stated
      preference onderzoeken uitgevoerd in het buitenland die houvast kunnen
      bieden om een eerste stap te kunnen zetten om toch tot dit inzicht te
      komen.<br />
    </p>
    <i>1.2 Maar we hebben toch een verkeersmodel?</i><br />
    <p>
      Mobiliteit is een gevolg van gedragskeuzen van reizigers. De meeste
      strategische verkeersmodellen bevatten gedragskeuzemodellen die de
      reisfrequentie-, vervoerwijze-, bestemmings-, vertrektijdstip- en
      routekeuze beschrijven. Daarnaast maken ze gebruik van data of
      deelmodellen die langere termijn keuzen beschrijven die de context
      schetsen waarin de gedragskeuzemodellen hun werk doen. Het gaat hierbij om
      zaken als woonlocatie, auto- en rijbewijsbezit.<br /><br />
      Mobility as a service heeft in potentie effect op alle genoemde
      gedragskeuzen, maar is alleen relevant wanneer reizigers in het bezit zijn
      van een MaaS abonnement. De keuze voor aanschaf van een MaaS-abonnement is
      dus rand voorwaardelijk aan deze effecten en kan door een verkeersmodel
      niet voorspeld worden.<br /><br />
      In dit paper wordt de MaaS potentiescan beschreven: een model wat het
      verwachte MaaS-abonnementsbezit voorspelt voor 128 verschillende
      persoonssegmenten voor elke CBS buurt in Nederland (waarvan er ca 13.000
      bestaan), gegeven een door de beleidsmaker ingegeven MaaS propositie en
      het huidige mobiliteitsgedrag op basis van digitale netwerken, gsm-data en
      technieken uit strategische verkeersmodellen.<br />
    </p>
    <h6><b>2. Van concept naar potentie</b></h6>
    <i>2.1 Waarom een potentiescan: het theoretisch denkkader</i><br />
    <p>
      Het is nog onzeker hoe MaaS en de invulling van het MaaS aanbod er
      specifiek in Nederland uit zal komen te zien en hoe het zich ontwikkeld op
      de langere termijn. Daarbij (of misschien wel daarom) is er in Nederland
      nog geen onderzoek gedaan naar verwachte gedragsveranderingen als gevolg
      van de introductie van MaaS. Hierdoor zijn er in Nederland ook nog geen
      keuzemodellen geschat om dergelijke veranderingen (mobiliteitseffecten) te
      kunnen modelleren. In het buitenland zijn wel enkele marktonderzoeken voor
      MaaS abonnementen uitgevoerd. Deze worden in deze paragraaf besproken.<br /><br />
      In (Ratilainen, 2017) is een stated preference onderzoek naar de
      voorkeuren van reizigers voor de invulling van MaaS abonnementen met een
      vaste maandprijs beschreven. Aan het onderzoek deden 252 respondenten uit
      de agglomeratie Helsinki mee. Uit dit onderzoek komt (onder andere) naar
      voren dat de abonnementskosten, de mate waarin openbaar vervoer opgenomen
      is in het pakket, of er sprake is van een gegarandeerde maximum wachttijd
      (voor deelauto’s en taxi’s), de leeftijd, het inkomen en de mogelijkheid
      om het abonnement te delen met andere leden in het huishouden bepalend
      zijn bij de keuze voor een MaaS abonnement. Opvallend is dat het opnemen
      van taxi en deelfietsen in de propositie geen noemenswaardig effect heeft
      en dat het opnemen van deelauto’s zelfs een licht negatief effect
      heeft.<br /><br />
      In hetzelfde onderzoek is een literatuurscan gedaan en zijn interviews
      uitgevoerd waarin onderzocht is wat mogelijke verklarende variabelen zijn
      bij de keuze om een MaaS abonnement te kopen. Uit de literatuur kwam naar
      voren dat het opnemen van taxi, carpool, (verschillende typen) openbaar
      vervoer, huurauto, deelfiets, deelauto, vliegtuig en zelfrijdende auto’s
      in het abonnement invloed kunnen hebben. Daarbij werden de prijs, de
      mogelijkheid om het abonnement te delen binnen het huishouden, de belofte
      van een maximum wachttijd, de mogelijkheid om een abonnement af te nemen
      waarbij per gebruikte dienst wordt afgerekend (‘Pay-As-You-Go’), het
      worden beloond bij gebruik van duurzame vervoerwijzen en de mogelijkheid
      om het abonnement als onderdeel van de huur te betalen genoemd.<br /><br />
      Aanvullend kwamen uit de interviews de volgende relevante abonnement
      eigenschappen naar voren: het kunnen kiezen van het type (deel/huur) auto,
      de mogelijkheid voor kinderzitjes, verschillende betalingstermijntypen en
      alternatieve afrekeningsmechanismen, het delen van het abonnement met
      personen buiten het huishouden, het maandelijks kunnen aanpassen van het
      abonnement en of de mobiliteitsverstrekker wordt beboet bij het niet
      nakomen van een afspraak.<br /><br />
      In een stated preference onderzoek van (Ho et al., 2017) naar MaaS
      abonnementsvoorkeuren onder 252 winkelende respondenten in Sydney werden
      naast abonnementen met een vaste prijs voor een bepaalde mobiliteitsbundel
      ook abonnementen 5 waarin moet worden betaald naar gebruik beschouwd. In
      dit onderzoek werd het huidige mobiliteitsgedrag expliciet meegenomen en
      lag meer nadruk op de eigenschappen van de respondent en zijn huishouden.
      Uit dit onderzoek komt naar voren dat het type abonnement, de
      abonnementskosten, de mate waarin openbaar vervoer en deelauto’s zijn
      opgenomen in het pakket, het (huidige) auto en openbaar vervoer gebruik,
      de huidige totale kosten voor mobiliteit, de leeftijd en het aantal
      kinderen in het huishouden bepalend zijn voor de keuze. Daarbij bleek de
      wachttijd voor deelauto’s en of ongebruikt krediet in het abonnement
      vervalt niet significant.<br /><br />
      In Tabel 1 is voor alle beschouwde variabelen de significantie volgens de
      twee in deze paragraaf beschouwde studies weergegeven. Lege cellen in deze
      tabel duiden erop dat de variabele in de betreffende stated preference
      studie niet is beschouwd, maar wel een mogelijk relevante variabele is
      volgens experts of andere literatuur. Op basis van de aangehaalde
      literatuur concluderen wij dat voor wat betreft de het MaaS
      abonnementsbezit er een vrij duidelijk beeld is van de relevante
      variabelen, en dat de definitie van de MaaS propositie en de context
      waarin deze aangeboden wordt (de bestaande vervoerwijze alternatieven, het
      beschouwd reismotief en kenmerken van het huishouden en de persoon) in
      grote mate bepalend is.<br /><br />
