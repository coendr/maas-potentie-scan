    <i>2.2 Bron voor gedragsparameter</i><br />
    <p>
      Om het verwachte MaaS abonnementsbezit te bepalen is idealiter revealed
      preference data uit een enquête zoals OViN nodig waarin een steekproef van
      Nederlanders een dagboek van hun gedragskeuzen omtrent mobiliteit
      bijhoudt. MaaS is echter nog maar zeer beperkt beschikbaar in Nederland:
      Tranzer (http://www.tranzer.com) biedt sinds eind 2017 OV en taxi diensten
      aan en MaaS Global / Whim (https://whimapp.com/nl/) geeft aan ‘binnenkort’
      in regio Amsterdam te beginnen. Het zal daarom nog even duren voordat
      dergelijke data er is.<br /><br />
      Een goede alternatieve databron is stated preference onderzoek, waarin
      respondenten fictieve keuzesituaties wordt voorgelegd en wordt gevraagd
      hun favoriete keuze kenbaar te maken. Op basis van dit soort onderzoeken
      kunnen gedragskeuzemodellen worden geschat die het verband tussen
      eigenschappen van de respondent (bijvoorbeeld: leeftijd, inkomen en
      huishoudgrootte), het huidige alternatief (bijvoorbeeld de huidige
      vervoerwijze en de huidige mobiliteitskosten) en de verschillende
      voorgelegde alternatieven (bijvoorbeeld de ingegeven MaaS propositie) en
      de gemaakte keuze beschrijven.<br /><br />
      Omdat er nog geen data beschikbaar is voor de Nederlandse situatie is
      wordt voorlopig gebruik gemaakt van de parameters geschat in het onderzoek
      van (Ho et al., 2017), beschreven in paragraaf 2.1. Alhoewel de context
      (Sydney, Australië) duidelijk anders is, verwachten wij dat veel van de
      belangrijkste overwegingen bij gedragskeuzen overeen zullen komen. Daarbij
      kunnen duidelijk afwijkende omstandigheden (bijvoorbeeld: prijsniveau van
      taxi services, onkostenvergoedingen werkgevers, het effect van de Opal
      card in Sydney) meegenomen moeten worden bij het interpreteren van de
      resultaten. Belangrijker nog: door nu al een potentiescan te ontwikkelen
      wordt al ervaring opgedaan met het modelleren van MaaS abonnementsbezit in
      Nederland, wat inzichten op zal leveren die van pas komen bij het
      ontwerpen van een toekomstige stated preference enquête naar MaaS en bij
      het schatten van gedragskeuzemodellen representatief voor de Nederlandse
      situatie. Bovendien kan zeer snel nadat de Nederlandse
      gedragskeuzemodellen geschat zijn de vertaalslag naar een toepasbare tool
      voor heel Nederland worden gemaakt.<br /><br />
    </p>
