      data uit de in ontwikkeling zijnde OmniTRANS-neXt module die de Common
      Operational Picture (COP)<sup>1</sup> beschrijft.<br />
      <sup>1</sup> COP : is een volledig beeld van een bepaalde situatie, op
      basis van meetgegevens en model- en data fusie technieken.<br /><br />
